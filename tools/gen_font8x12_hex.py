def to_8x12(rows_5x7):
    out = []
    for r in range(12):
        src_r = (r * 7) // 12
        src = rows_5x7[src_r]
        bits = 0
        for c in range(5):
            if src[c] == "#":
                dst_c = c + 1  # center 5 cols in 8-bit row
                bits |= 1 << (7 - dst_c)
        out.append(bits)
    return out


def build_rom():
    rom = [0] * (128 * 12)
    for code in range(128):
        ch = chr(code)
        rows = GLYPH_5X7.get(ch, GLYPH_5X7[" "])
        glyph = to_8x12(rows)
        base = code * 12
        for i, b in enumerate(glyph):
            rom[base + i] = b
    return rom


def main():
    root = Path(__file__).resolve().parents[1]
    out_path = root / "font8x12.hex"
    rom = build_rom()
    with out_path.open("w", encoding="ascii") as f:
        for b in rom:
            f.write(f"{b:02x}\n")
    print(f"generated {out_path} ({len(rom)} bytes)")


if __name__ == "__main__":
    main()

